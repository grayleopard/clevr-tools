/** Default fallback icon when a tool's icon string isn't found */
export const defaultToolIcon: IconComponent = Wrench;

/** Resolve a tool icon string to a component */
export function getToolIcon(iconName: string): IconComponent {
  return toolIconMap[iconName] ?? defaultToolIcon;
}
